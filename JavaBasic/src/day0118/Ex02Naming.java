// 이름에 변형을 가하거나 동의어를 사용하면 된다.

//낙타등 표기법과 뱀 표기법
//1. 낙타등 표기법(Camel back notation)
// 낙타등 표기법이란 어떠한 것에 이름을 지어줄때
// 여러 단어로 이루어져 있을 경우,
// 두번째 단어부터는 첫글자를 대문자로 적어줘서
// 몇개의 단어로 이루어져있는지 쉽게 알려주는 표기법이다.
// 예시 : userNumber, memberController, UserMemberAuthController
//       앞에 두개만 변수

//2. 뱀 표기법 (Snake notation)
// 뱀 표기법이란 어떠한 것에 이름을 지어줄때
// 모든 글자를 대문자로 적어주고
// 여러 단어일 경우 단어의 사이마다 _ 를 넣어줘서 몇개의 단어로 이루어져있는지
// 쉽게 알려주는 표기법이다.
// 예시 : UNIT_PRICE, MEMBER_PICTURE_ADDRESS (상수)

