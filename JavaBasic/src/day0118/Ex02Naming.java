//4. 한글로 이름을 지어줄 수 있기는 하지만 절대로 그러지 말자
//5. 자바에는 내부적으로 기능이 설정된 50여가지의 "예약어(keyword)"가 있다.
// 해당 단어들로는 우리가 이름을 지어줄 수 없기 때문에
// 여러분들은 그 50여가지를 외우셔야 한다.
// 내일 11시에 시험보겠습니다.
// https://en.wikipedia.org/wiki/List_of_Java_keywords
// 이클립스로 대표되는 IDE가 나오기 전에는 우리가 이런식으로 키워드를 외워야만 했다.
// 이클립스의 경우, 키워드를 자주색으로 표시해준다.
// 즉 public, class 등의 단어들은 자주색으로 표시되므로 여러분들이 키워드라는 것을
// 알 수 있다.
